class ForEx {

  public static void main(String args[]) {

  int i;

  for (i=0;i<5;++i)
  System.out.println(i);
  System.out.println("the value of i is "+i);

  System.out.println("now let's print it backwards ");

  for (i=5;i>=0;--i)
  System.out.println(i);
  System.out.println("the value of i is "+i);
  }//main

}//ForEx
